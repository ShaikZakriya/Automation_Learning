package java_Learning;

public class AverageMarksStudent {

	public static void main(String[] args) {
		
		int zakriyamarks[] = {46, 76, 66, 88};
		int sum = zakriyamarks[0] + zakriyamarks[1] + zakriyamarks[2] + zakriyamarks[3];
		double average = sum / 4.0;
		System.out.println("The average of Zakriya's marks is: " + average);
		
		
		int lokeshmarks[] = {56, 86, 96, 78};
		int sum1 = lokeshmarks[0] + lokeshmarks[1] + lokeshmarks[2] + lokeshmarks[3];
		double average1 = sum1 / 4.0;
		System.out.println("The average of Lokesh's marks is: " + average1);
	        

	}
	

}
